odd_max = int(input("Пожалуйста, введите число, до которого нужно сгенерировать нечетные числа: "))
odd_to_n = (num for num in range(1, odd_max + 1) if num % 2 != 0)  # генератор нечетных чисел в заданном диапазоне
print(*odd_to_n)  # вывод распакованного генератора
